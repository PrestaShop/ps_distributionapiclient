declare(strict_types=1);

namespace PrestaShop\Module\DistributionApiClient\Form\Type;

use PrestaShopBundle\Form\Admin\Type\SwitchType;
use Symfony\Component\Form\AbstractType;
use Symfony\Component\Form\FormBuilderInterface;
use Symfony\Contracts\Translation\TranslatorInterface;

class ConfigurationType extends AbstractType
{
    private TranslatorInterface $translator;

    public function __construct(TranslatorInterface $translator)
    {
        $this->translator = $translator;
    }

    public function buildForm(FormBuilderInterface $builder, array $options): void
    {
        $builder
            ->add('wall_of_fame_enabled', SwitchType::class, [
                'label' => $this->translator->trans('Enable Wall of Fame Tab', [], 'Modules.Distributionapiclient.Admin'),
                'help' => $this->translator->trans('Enable or disable the Wall of Fame tab in the back office menu under Community.', [], 'Modules.Distributionapiclient.Admin'),
                'required' => false,
            ]);
    }
}
